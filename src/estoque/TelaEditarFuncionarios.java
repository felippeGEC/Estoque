private void refleshTabela() {
        this.dtm.getDataVector().removeAllElements();
        fun = (arquivoFun.ler());
        if (fun.size() > 0) {
            for (Funcionario funcionario : fun) {
                dtm.insertRow(dtm.getRowCount(), new Object[]{
                    funcionario.getNome(),
                    funcionario.getSexo(),
                    funcionario.getCpf(),
                    funcionario.getCep(),
                    funcionario.getEmail(),
                    funcionario.getCargo(),
                    funcionario.getSalario()
                });
            }
        } else {
            dtm.insertRow(dtm.getRowCount(), new Object[]{
                "",
                "",
                "",
                "",
                "",
                "",
                ""
            });
        }
    }
